    private void registerQueue() {
        final int size = queueService.getVector().size() + 1;
        if (size > 20) {
            JOptionPane.showConfirmDialog(this, "numero maximo de tickets alcanzados", "limite excedido", JOptionPane.ERROR_MESSAGE);
        }
        final String value = JOptionPane.showInputDialog(this, "ingrese el tiket numero " + size, "Ingresar", JOptionPane.CLOSED_OPTION);
        queueService.getVector().add(value);
        information.getComponentShow().setListData(queueService.getVector());

    }

    private void registerStack() {
        final int size = stackService.getVector().size();
        final int index = 20 - size;
        if (size > 19) {
            JOptionPane.showConfirmDialog(this, "numero maximo de tickets alcanzados", "limite excedido", JOptionPane.ERROR_MESSAGE);
            return;
        }
        final String value = JOptionPane.showInputDialog(this, "ingrese el tiket numero " + index, "Ingresar", JOptionPane.CLOSED_OPTION);
        stackService.getVector().add(value);
        setStack(stackService.getVector());
    }

