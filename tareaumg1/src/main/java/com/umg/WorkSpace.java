    private void showQueues() {
        information.getComponentShow().setListData(queueService.getVector());
        information.setLocationRelativeTo(null);
        information.setVisible(true);
    }

    private void showStack() {
        setStack(stackService.getVector());
        information.setLocationRelativeTo(null);
        information.setVisible(true);
    }

    private void getTicketQueue() {
        final int size = queueService.getVector().size();
        final int index = 21 - size;

        if (size == 0) {
            JOptionPane.showConfirmDialog(this, "sin ticket pendientes", "sin tickets", JOptionPane.CLOSED_OPTION);
            return;
        }

        Object ticket = queueService.getVector().firstElement();
        JOptionPane.showConfirmDialog(this, "atendiendo: " + ticket, "atender ticket", JOptionPane.CLOSED_OPTION);
        queueService.getVector().remove(0);
        information.getComponentShow().setListData(queueService.getVector());

    }

    private void getTicketStack() {
        final int size = stackService.getVector().size();
        final int index = size - 1;

        if (size == 0) {
            JOptionPane.showConfirmDialog(this, "sin ticket pendientes", "sin tickets", JOptionPane.CLOSED_OPTION);
            return;
        }

        Object ticket = stackService.getVector().get(index);
        JOptionPane.showConfirmDialog(this, "atendiendo: " + ticket, "atender ticket", JOptionPane.CLOSED_OPTION);
        stackService.getVector().remove(index);
        setStack(stackService.getVector());

    }

    private void setStack(Vector vector) {
        int size = vector.size();
        Vector order = new Vector();
        for (int i = size; i >= 1; i--) {
            order.add(vector.get(i - 1));
        }
        information.getComponentShow().setListData(order);
    }

